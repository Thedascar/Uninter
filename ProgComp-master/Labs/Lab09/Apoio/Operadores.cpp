#include <iostream>
using namespace std;

int main()
{
	float num1, num2;

	cout << "Entre com um numero: ";
	cin >> num1;
	cout << "Entre com outro numero: ";
	cin >> num2;

	cout << "num1 = " << num1 << "; num2 = " << num2 << endl;
	cout << "num1 + num2 = " << num1 + num2 << endl;
	cout << "num1 - num2 = " << num1 - num2 << endl;
	cout << "num1 * num2 = " << num1 * num2 << endl;
	cout << "num1 / num2 = " << num1 / num2 << endl;

	return 0;
}