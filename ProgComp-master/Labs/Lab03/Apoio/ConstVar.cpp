// constvar.cpp – exemplifica o uso de constantes
#include <iostream>
using namespace std;

int main()
{
	cout << "A letra " << 'm';
	cout << " tem " << 3 << " pernas." << endl;

	char ch = 'M';              // tipo caractere
	int  num = 0x1E;            // tipo inteiro

	cout << "Dez letras " << ch;
	cout << " tem " << num << " pernas." << endl;

	return 0;
}
